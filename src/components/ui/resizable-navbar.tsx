export const NavbarButton = ({
  href,
  as: Tag = "a",
  children,
  className,
  variant = "primary",
  ...props
}: {
  href?: string;
  as?: React.ElementType;
  children: React.ReactNode;
  className?: string;
  variant?: "primary" | "secondary" | "dark" | "gradient";
} & (
  | React.ComponentPropsWithoutRef<"a">
  | React.ComponentPropsWithoutRef<"button">
)) => {
  const baseStyles =
    "px-4 py-2 rounded-md text-sm font-semibold transition duration-200 inline-block text-center cursor-pointer";

  const variantStyles = {
    primary:
      "bg-accent-blue text-primary-foreground hover:bg-accent-blue/80",
    secondary:
      "bg-transparent text-secondary hover:text-primary",
    dark:
      "bg-surface text-primary hover:bg-card",
    gradient:
      "bg-gradient-to-r from-accent-blue to-accent-purple text-primary-foreground",
  };

  return (
    <Tag
      href={href || undefined}
      className={cn(baseStyles, variantStyles[variant], className)}
      {...props}
    >
      {children}
    </Tag>
  );
};
